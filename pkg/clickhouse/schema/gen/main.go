// Command gen writes columns_generated.go: the Row implementation (Table and
// InsertColumns methods) for every schema struct carrying an //unkey:table
// directive in its doc comment, so INSERT statements can name their target
// table and columns statically (see the Row interface).
//
// Column derivation mirrors clickhouse-go's struct mapping (struct_map.go):
// the `ch` tag overrides the field name, `ch:"-"` and unexported fields are
// skipped. Embedded fields are rejected because no schema struct uses them;
// support flattening here if that changes.
//
// Structs without a directive are ignored: they are scan targets or typed
// JSON payloads, not insertable rows.
package main

import (
	"fmt"
	"go/ast"
	"go/parser"
	"go/token"
	"os"
	"reflect"
	"strings"
)

const (
	outputFile     = "columns_generated.go"
	tableDirective = "//unkey:table"
)

func main() {
	src, err := generate(".")
	if err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
	if err := os.WriteFile(outputFile, []byte(src), 0o644); err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
}

// generate parses the schema package in dir and renders the generated file.
func generate(dir string) (string, error) {
	entries, err := os.ReadDir(dir)
	if err != nil {
		return "", fmt.Errorf("reading %s: %w", dir, err)
	}
	fset := token.NewFileSet()
	var files []*ast.File
	for _, entry := range entries {
		name := entry.Name()
		if entry.IsDir() || !strings.HasSuffix(name, ".go") ||
			strings.HasSuffix(name, "_test.go") || name == outputFile {
			continue
		}
		file, err := parser.ParseFile(fset, dir+"/"+name, nil, parser.ParseComments|parser.SkipObjectResolution)
		if err != nil {
			return "", fmt.Errorf("parsing %s: %w", name, err)
		}
		files = append(files, file)
	}

	var b strings.Builder
	b.WriteString("// Code generated by go run ./gen. DO NOT EDIT.\n\npackage schema\n")

	for _, file := range files {
		for _, decl := range file.Decls {
			genDecl, ok := decl.(*ast.GenDecl)
			if !ok || genDecl.Tok != token.TYPE {
				continue
			}
			for _, spec := range genDecl.Specs {
				typeSpec, ok := spec.(*ast.TypeSpec)
				if !ok {
					continue
				}
				structType, ok := typeSpec.Type.(*ast.StructType)
				if !ok {
					continue
				}
				table, err := tableName(genDecl, typeSpec)
				if err != nil {
					return "", fmt.Errorf("%s: %w", typeSpec.Name.Name, err)
				}
				if table == "" {
					continue
				}
				columns, err := deriveColumns(structType)
				if err != nil {
					return "", fmt.Errorf("%s: %w", typeSpec.Name.Name, err)
				}
				if len(columns) == 0 {
					return "", fmt.Errorf("%s: has an %s directive but no ch-tagged fields", typeSpec.Name.Name, tableDirective)
				}
				quoted := make([]string, len(columns))
				for i, c := range columns {
					quoted[i] = "`" + c + "`"
				}
				fmt.Fprintf(&b, "\n// Table implements [Row].\nfunc (%s) Table() string {\n\treturn \"%s\"\n}\n",
					typeSpec.Name.Name, table)
				fmt.Fprintf(&b, "\n// InsertColumns implements [Row]; derived from %s's ch tags.\nfunc (%s) InsertColumns() string {\n\treturn \"%s\"\n}\n",
					typeSpec.Name.Name, typeSpec.Name.Name, strings.Join(quoted, ", "))
			}
		}
	}
	return b.String(), nil
}

// tableName extracts the //unkey:table directive from the type's doc comment.
// Returns "" when the type has no directive.
func tableName(genDecl *ast.GenDecl, typeSpec *ast.TypeSpec) (string, error) {
	doc := typeSpec.Doc
	if doc == nil {
		doc = genDecl.Doc
	}
	if doc == nil {
		return "", nil
	}
	for _, comment := range doc.List {
		if !strings.HasPrefix(comment.Text, tableDirective) {
			continue
		}
		table := strings.TrimSpace(strings.TrimPrefix(comment.Text, tableDirective))
		if table == "" || strings.ContainsAny(table, " \t`\"") {
			return "", fmt.Errorf("malformed directive %q, want %s <database.table>", comment.Text, tableDirective)
		}
		return table, nil
	}
	return "", nil
}

// deriveColumns returns the column names AppendStruct can populate for the
// struct, in field order.
func deriveColumns(structType *ast.StructType) ([]string, error) {
	var columns []string
	seen := make(map[string]bool)
	for _, field := range structType.Fields.List {
		if len(field.Names) == 0 {
			return nil, fmt.Errorf("embedded fields are not supported")
		}
		var tag string
		if field.Tag != nil {
			tag = reflect.StructTag(strings.Trim(field.Tag.Value, "`")).Get("ch")
		}
		if tag == "-" {
			continue
		}
		for _, ident := range field.Names {
			if !ident.IsExported() {
				continue
			}
			name := tag
			if name == "" {
				name = ident.Name
			}
			if !seen[name] {
				seen[name] = true
				columns = append(columns, name)
			}
		}
	}
	return columns, nil
}
